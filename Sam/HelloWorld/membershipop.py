# a membership operator checks if sequence is present in an object
# "in" is one of the membership operator
# creating a variable
my_list = ['samuel', 'osas', 'gideon', 'kelechi']

check = "kelechi"
print(check in my_list)



# "not in" is one of the membership operator
# creating a variable
my_list = ['samuel', 'osas', 'gideon', 'kelechi']

check = "kelechi"
print(check not in my_list)
